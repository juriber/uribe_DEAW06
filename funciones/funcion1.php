<?php
/**
 * Función que suma dos números.
 * 
 * Esta función toma dos números enteros como entrada y devuelve su suma.
 * 
 * @param int $num1 El primer número a sumar.
 * @param int $num2 El segundo número a sumar.
 * 
 * @return int El resultado de la suma de los dos números.
 * 
 * @example suma(5, 7) devolvería 12.
 */
function suma($num1, $num2) {
    return $num1 + $num2;
}
?>
