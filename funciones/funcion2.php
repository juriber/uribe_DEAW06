<?php
/**
 * Función que genera un saludo personalizado.
 * 
 * Esta función toma un nombre como entrada y devuelve un saludo con ese nombre.
 * 
 * @param string $nombre El nombre de la persona a saludar.
 * 
 * @return string El saludo personalizado.
 * 
 * @example saludo("Juan") devolvería "Hola, Juan".
 */
function saludo($nombre) {
    return "Hola, " . $nombre;
}
?>
